def sub(a1, b1):
    """
    This function returns the result of subtracting b1 from a1.

    :param a1: First input number.
    :type a1: int
    :param b1: Second input number.
    :type b1: int
    :return: The result of subtracting b1 from a1.
    :rtype: int
    """
    s1 = a1 - b1
    return s1
 
